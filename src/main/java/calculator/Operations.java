package calculator;

public class Operations {
    public static int getSum(int val1, int val2) {
        return val1 + val2;
    }

    public static int getSubtraction(int val1, int val2) {
        return val1-val2;
    }

    public static int getMultiplication(int val1, int val2) {
        return val1*val2;
    }

    public static int getDivision(int val1, int val2) {
        return val1/val2;
    }
}
